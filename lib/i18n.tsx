const I18nContext = createContext<I18nContextValue | undefined>(undefined);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<AppLanguage>("en");
  const [isLoadingLanguage, setIsLoadingLanguage] = useState(true);

  useEffect(() => {
    let isMounted = true;

    async function loadLanguage() {
      try {
        const stored = await settingsService.get<AppLanguage>(LANGUAGE_SETTING_KEY);

        if (isMounted && isAppLanguage(stored)) {
          setLanguageState(stored);
        }
      } finally {
        if (isMounted) {
          setIsLoadingLanguage(false);
        }
      }
    }

    loadLanguage();

    return () => {
      isMounted = false;
    };
  }, []);

  useEffect(() => {
    document.documentElement.lang = language;
    document.documentElement.dir = language === "fa" ? "rtl" : "ltr";
  }, [language]);

  const setLanguage = useCallback(async (nextLanguage: AppLanguage) => {
    setLanguageState(nextLanguage);
    await settingsService.set(LANGUAGE_SETTING_KEY, nextLanguage);
  }, []);

  const t = useCallback(
    (key: string, params: TranslationParams = {}) => {
      const template =
        translations[language][key] ?? translations.en[key] ?? key;

      return Object.entries(params).reduce(
        (value, [paramKey, paramValue]) =>
          value.replaceAll(`{${paramKey}}`, String(paramValue)),
        template,
      );
    },
    [language],
  );

  const formatNumber = useCallback(
    (value: number) => new Intl.NumberFormat(language === "fa" ? "fa-IR" : "en-US").format(value),
    [language],
  );

  const formatDate = useCallback(
    (date: Date | string, options: Intl.DateTimeFormatOptions = {}) =>
      new Intl.DateTimeFormat(language === "fa" ? "fa-IR" : "en-US", options).format(
        typeof date === "string" ? new Date(date) : date,
      ),
    [language],
  );

  const value = useMemo<I18nContextValue>(
    () => ({
      dir: language === "fa" ? "rtl" : "ltr",
      formatDate,
      formatNumber,
      isLoadingLanguage,
      language,
      setLanguage,
      t,
    }),
    [formatDate, formatNumber, isLoadingLanguage, language, setLanguage, t],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);

  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }

  return context;
}

export function translateGameText(value: string | undefined, language: AppLanguage) {
  if (!value || language === "en") {
    return value;
  }

  return gameTextTranslations[value] ?? value;
}

function isAppLanguage(value: unknown): value is AppLanguage {
  return value === "en" || value === "fa";
}
