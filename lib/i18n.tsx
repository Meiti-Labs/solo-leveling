const I18nContext = createContext<I18nContextValue | undefined>(undefined);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [hasLanguagePreference, setHasLanguagePreference] = useState(false);
  const [language, setLanguageState] = useState<AppLanguage>("en");
  const [isLoadingLanguage, setIsLoadingLanguage] = useState(true);

  useEffect(() => {
    let isMounted = true;

    async function loadLanguage() {
      try {
        const stored = await settingsService.get<AppLanguage>(LANGUAGE_SETTING_KEY);

        if (isMounted && isAppLanguage(stored)) {
          setLanguageState(stored);
          setHasLanguagePreference(true);
        }
      } finally {
        if (isMounted) {
          setIsLoadingLanguage(false);
        }
      }
    }

    loadLanguage();

    return () => {
      isMounted = false;
    };
  }, []);

  useEffect(() => {
    document.documentElement.lang = language;
    document.documentElement.dir = language === "fa" ? "rtl" : "ltr";
  }, [language]);

  const setLanguage = useCallback(async (nextLanguage: AppLanguage) => {
    await settingsService.set(LANGUAGE_SETTING_KEY, nextLanguage);
    setLanguageState(nextLanguage);
    setHasLanguagePreference(true);
  }, []);

  const t = useCallback(
    (key: string, params: TranslationParams = {}) => {
      const template =
        translations[language][key] ?? translations.en[key] ?? key;

      return Object.entries(params).reduce(
        (value, [paramKey, paramValue]) =>
          value.replaceAll(`{${paramKey}}`, String(paramValue)),
        template,
      );
    },
    [language],
  );

  const formatNumber = useCallback(
    (value: number) => new Intl.NumberFormat(language === "fa" ? "fa-IR" : "en-US").format(value),
    [language],
  );

  const formatDate = useCallback(
    (date: Date | string, options: Intl.DateTimeFormatOptions = {}) =>
      new Intl.DateTimeFormat(language === "fa" ? "fa-IR" : "en-US", options).format(
        typeof date === "string" ? new Date(date) : date,
      ),
    [language],
  );

  const value = useMemo<I18nContextValue>(
    () => ({
      dir: language === "fa" ? "rtl" : "ltr",
      formatDate,
      formatNumber,
      hasLanguagePreference,
      isLoadingLanguage,
      language,
      setLanguage,
      t,
    }),
    [
      formatDate,
      formatNumber,
      hasLanguagePreference,
      isLoadingLanguage,
      language,
      setLanguage,
      t,
    ],
  );

  return (
    <I18nContext.Provider value={value}>
      {isLoadingLanguage ? (
        <div className="min-h-svh bg-background" />
      ) : hasLanguagePreference ? (
        children
      ) : (
        <LanguageGate />
      )}
    </I18nContext.Provider>
  );
}

function LanguageGate() {
  const { setLanguage } = useI18n();
  const [error, setError] = useState<string | null>(null);
  const [savingLanguage, setSavingLanguage] = useState<AppLanguage | null>(null);

  async function chooseLanguage(nextLanguage: AppLanguage) {
    try {
      setError(null);
      setSavingLanguage(nextLanguage);
      await setLanguage(nextLanguage);
    } catch {
      setError("Could not save language. لطفا دوباره تلاش کن.");
    } finally {
      setSavingLanguage(null);
    }
  }

  return (
    <main className="flex min-h-svh items-center justify-center bg-background px-4 py-8 text-foreground">
      <section className="w-full max-w-sm space-y-6">
        <div className="space-y-2 text-center">
          <p className="text-sm font-medium uppercase tracking-[0.16em] text-[#5aa0ff]">
            Solo Leveling
          </p>
          <h1 className="text-3xl font-semibold leading-tight text-white">
            Choose language
          </h1>
          <p className="text-2xl font-semibold leading-tight text-white" dir="rtl">
            انتخاب زبان
          </p>
        </div>

        <div className="grid gap-3">
          <button
            className="min-h-16 rounded-2xl border border-[#2f8cff]/70 bg-[#0d4fe0] px-4 text-left text-white shadow-[0_0_22px_rgba(47,140,255,0.36)] transition hover:bg-[#155df0] disabled:opacity-60"
            disabled={Boolean(savingLanguage)}
            onClick={() => chooseLanguage("en")}
            type="button"
          >
            <span className="block text-lg font-semibold">English</span>
            <span className="block text-sm text-blue-100/80">
              {savingLanguage === "en" ? "Saving..." : "Continue in English"}
            </span>
          </button>

          <button
            className="min-h-16 rounded-2xl border border-slate-700/70 bg-[#07111f]/82 px-4 text-right text-white shadow-[0_10px_28px_rgba(0,0,0,0.28),inset_0_1px_18px_rgba(99,148,216,0.06)] transition hover:border-[#2f8cff]/70 hover:bg-[#0b1728] disabled:opacity-60"
            dir="rtl"
            disabled={Boolean(savingLanguage)}
            onClick={() => chooseLanguage("fa")}
            type="button"
          >
            <span className="block text-lg font-semibold">فارسی</span>
            <span className="block text-sm text-slate-300">
              {savingLanguage === "fa" ? "در حال ذخیره..." : "ادامه با فارسی"}
            </span>
          </button>
        </div>

        {error && (
          <p className="rounded-xl border border-rose-500/50 bg-rose-950/25 px-3 py-2 text-center text-sm text-rose-100">
            {error}
          </p>
        )}
      </section>
    </main>
  );
}

export function useI18n() {
  const context = useContext(I18nContext);

  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }

  return context;
}

export function translateGameText(value: string | undefined, language: AppLanguage) {
  if (!value || language === "en") {
    return value;
  }

  return gameTextTranslations[value] ?? value;
}

function isAppLanguage(value: unknown): value is AppLanguage {
  return value === "en" || value === "fa";
}
